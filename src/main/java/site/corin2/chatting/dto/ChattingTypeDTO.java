package site.corin2.chatting.dto;

public class ChattingTypeDTO {

	private String chattingNum;
	private String chattingName;
	
	public ChattingTypeDTO() {}
	
	public String getChattingName() {
		return chattingName;
	}
	public void setChattingName(String chattingName) {
		this.chattingName = chattingName;
	}

	public String getChattingNum() {
		return chattingNum;
	}

	public void setChattingNum(String chattingNum) {
		this.chattingNum = chattingNum;
	}
	
	
}
